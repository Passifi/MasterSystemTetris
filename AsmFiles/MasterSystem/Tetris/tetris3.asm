readMelodyStream: 
	ld a,(MelodyIndex)
	ld c,a 
	ld b,0
	ld hl, Melody 
	add hl, bc 
	call updatePSG
	and &60 
	cp &60 
	jp nz,twoChannelByte
oneChannelByte:
	inc hl 
	inc c 
	call updatePSG
	jp setTimer

twoChannelByte: 
	inc hl 
	inc c 
	call updatePSG
	inc hl 
	inc c 
	call updatePSG

setTimer:
	inc hl 
	inc c 
	ld a,(hl)
	ld (TimeInterval),a
	bit 7,a 
	jp nz, setTimer
	inc c 
	ld a,c
	cp EndOfMelody - Melody ;reset a 
	jp c, endLine
	ld a, 0
endLine:
	ld (MelodyIndex),a 
	ret 

	


	


	

