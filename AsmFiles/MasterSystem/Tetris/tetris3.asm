	ld (ticks),a 
	or a 
	cp 0
	ld a,(MelodyCounter)
	ld b,a 
	ld hl,Melody
	ld a,l 
	add b 
	ld l,a 
	ld a,(hl)
	ld (Soundbank+1),a 
	inc hl 
	ld a,(hl)
	ld (Soundbank+2),a 
	ld a,b 
	inc a
	inc a 
	ld (MelodyCounter),a 



	
	call updatePSG
	jp z, interuptReturn
	



interuptReturn: 
	ei
	ret 
	

	
